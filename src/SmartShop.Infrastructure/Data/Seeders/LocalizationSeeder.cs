    public int Order => 0;  // Run first before other seeders

    public async Task SeedAsync(CancellationToken cancellationToken = default)
    {
        logger.LogInformation("Seeding localization tables...");

        var allMessages = GetLocalizedMessages();
        var allFieldNames = GetLocalizedFieldNames();

        // Idempotent upsert — only insert keys that don't exist yet
        var existingMsgKeys = await context.LocalizedMessages
            .AsNoTracking()
            .Select(m => new { m.MessageKey, m.Language })
            .ToListAsync(cancellationToken);

        var existingMsgSet = existingMsgKeys
            .Select(x => $"{x.MessageKey}:{x.Language}")
            .ToHashSet();

        var newMessages = allMessages
            .Where(m => !existingMsgSet.Contains($"{m.MessageKey}:{m.Language}"))
            .ToList();

        var existingFieldKeys = await context.LocalizedFieldNames
            .AsNoTracking()
            .Select(f => new { f.FieldKey, f.Language })
            .ToListAsync(cancellationToken);

        var existingFieldSet = existingFieldKeys
            .Select(x => $"{x.FieldKey}:{x.Language}")
            .ToHashSet();

        var newFieldNames = allFieldNames
            .Where(f => !existingFieldSet.Contains($"{f.FieldKey}:{f.Language}"))
            .ToList();

        if (newMessages.Count == 0 && newFieldNames.Count == 0)
        {
            logger.LogInformation("Localization tables already up to date.");
            return;
        }

        if (newMessages.Count > 0)
            await context.LocalizedMessages.AddRangeAsync(newMessages, cancellationToken);

        if (newFieldNames.Count > 0)
            await context.LocalizedFieldNames.AddRangeAsync(newFieldNames, cancellationToken);

        await context.SaveChangesAsync(cancellationToken);

        logger.LogInformation("Seeded {MessageCount} new localized messages and {FieldCount} new field names.",
            newMessages.Count, newFieldNames.Count);
    }

